        <p>Слика углавном уља на платну ,али и аквареле .Нема посебан стил јер има великих пауза у сликању .Кад почне да слика све је другачије ,и тема и начин .Иде од реализма , експресионизма ,симболизма .</p>
        <p>Нема тачну евиденцију о броју слика и броја изложби .Насликала је преко 500 слика на уљу ,а акварела безброј .Слика и данас.</p>
